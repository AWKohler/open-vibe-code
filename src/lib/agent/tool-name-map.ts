/** ID validation per Anthropic's tool_use_id constraint. */
export const TOOL_USE_ID_PATTERN = /^[a-zA-Z0-9_-]+$/;

/** Replace any characters outside the allowed set with `-` so a malformed
 *  legacy id doesn't crash a fresh API request. Deterministic, so retries
 *  remain idempotent. */
export function sanitizeToolUseId(id: string): string {
  if (!id) return "tool_unknown";
  if (TOOL_USE_ID_PATTERN.test(id)) return id;
  return id.replace(/[^a-zA-Z0-9_-]/g, "-");
}
